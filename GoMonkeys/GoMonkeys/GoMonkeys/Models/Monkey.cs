using Microsoft.WindowsAzure.MobileServices;
using Newtonsoft.Json;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace GoMonkeys.Models
{
    public class Monkey
    {
        [JsonProperty(PropertyName = "id")]
